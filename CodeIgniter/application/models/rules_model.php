class Rules_model extends CI_Model
{
    function getRules() 
    {
        $this->db->select()->from('rules');
        $query = $this->db->get();

        if ($query->num_rows() > 0) {
            foreach ($query->result() as $row) {
                $data[] = $row;
            }
            return $data;
        }
    }
}

